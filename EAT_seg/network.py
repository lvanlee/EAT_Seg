class BasicConv2d(nn.Module):
    def __init__(self, ch_in, ch_out, **kwargs):
        super(BasicConv2d, self).__init__()
        self.conv = nn.Conv2d(ch_in, ch_out, **kwargs)
        self.bn = nn.BatchNorm2d(ch_out)

    def forward(self, x):
        out = self.conv(x)
        out = self.bn(out)
        return out
       



class DB(nn.Module):
    
    def __init__(self, ch_in, ch_out):
        super(DB, self).__init__()
        self.conv1 = nn.Sequential(
            nn.Conv2d(ch_in, ch_out // 4, kernel_size=3, stride=1, dilation=1, padding=1),
            torch.nn.ReLU())
        self.conv2 = nn.Sequential(
            nn.Conv2d(ch_in, ch_out // 4, kernel_size=3, stride=1, dilation=2, padding=2),
            torch.nn.ReLU())
        self.conv3 = nn.Sequential(
            nn.Conv2d(ch_in, ch_out // 4, kernel_size=3, stride=1, dilation=4, padding=4),
            torch.nn.ReLU())
        self.conv4 = nn.Sequential(
            nn.Conv2d(ch_in, ch_out // 4, kernel_size=3, stride=1, dilation=8, padding=8),
            torch.nn.ReLU())

    def forward(self, x):
        conv1_output = self.conv1(x)
        conv2_output = self.conv2(x)
        conv3_output = self.conv3(x)
        conv4_output = self.conv4(x)

        output = torch.cat((conv1_output, conv2_output, conv3_output, conv4_output), 1)
        return output




